public class Ed_10_Operator {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		char a = 'a'; // ASCII 코드 97
		char d = 'd'; // ASCII 코드 100
		
		char zero = '0'; // ASCII 코드 48
		char two = '2'; // ASCII 코드 50
		
		System.out.println("================================");
		System.out.printf("'%c' - '%c' = %d\n", d, a, (d-a));
		System.out.printf("'%c' - '%c' = %d\n", two, zero, (two-zero));
		
		
		System.out.println("================================");
		
		

	}

}
