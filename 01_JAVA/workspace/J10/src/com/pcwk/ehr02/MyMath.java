package com.pcwk.ehr02;

public class MyMath {
	
	/**
	 * 덧셈
	 * @param a
	 * @param b
	 * @return a+b
	 */
	long add(long a, long b) {
		long result = a + b;
		
		return result;
	}
	
	/**
	 * 뺄셈
	 * @param a
	 * @param b
	 * @return a-b
	 */
	long substract(long a, long b) {
		return a - b;
			
	}
	
	/**
	 * 곱하기
	 * @param a
	 * @param b
	 * @return a*b
	 */
	long multiply(long a, long b) {
		return a * b;
	}
	
	/**
	 * 나누기
	 * @param a
	 * @param b
	 * @return a/b
	 */
	double divine(double a, double b) {
		return a / b;
	}

}
