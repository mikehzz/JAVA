package com_pcwk_ehr;

public class Ed_05_Operator {

	public static void main(String[] args) {
		int num1 = 8;
		int result = num1 << 2;
		
		System.out.println("num1 : "+num1);
		System.out.println("num1 << 2 : " + result);
		
		result = num1 >> 2;
		System.out.println("num1 >> 2 : "+result);
		
		
		
	}

}
