package com_pcwk_ehr;

public class Ed_06_Operator {

	public static void main(String[] args) {

		// 대입연산자는 값을 할당하는데 쓰이는 연산자로, 
		// 연산을 간결하게 표현가능.
		
		int a = 3;
		int b = 5;
		
		b = a;
		System.out.printf("b=%d\n",b);
		a+=1;// a=a+1
		System.out.printf("a+= 1 -> %d\n", a);
		
		a=3;
		a*=2;// a=a*2
		System.out.printf("a*=2 -> %d\n", a);
		
		
		
		
		
		
	}

}
