package com_pcwk_ehr;

public class Ed_11_Arithmetic {

	public static void main(String[] args) {

		int x = 10;
		int y = 3;
		
		System.out.printf("x + y = %d%n", x+y);
		System.out.printf("x + y = %d\n", x-y);
		System.out.printf("x*y = %d\n", x*y);
		
		System.out.printf("x/y = %d\n", x/y);// 10/3 -> 3
		System.out.printf("x%%y = %d\n", x%y);// 10%3 -> 1
		
		// %하나를 출력 하면 에러가 뜨기 때문에 두개를 붙여준다.

		
		
		
		
	}

}
