package com_pcwk_ehr;

public class Ed_01_Char {

	public static void main(String[] args) {
		char ch01 = 'A';
		System.out.println("==============");
		System.out.println("ch01 : " + ch01);// 문자 출력
		System.out.println("ch01 ASCII 코드 : " + (int)ch01);// 문자에 대한 ASCII 코드 값 출력
		
		//char 변수에 ASCII 코드 값 할당
		char ch02 = 66;
		System.out.println("ch02 : " + ch02);
		System.out.println("==============");
		
		
		
		
	}
}


